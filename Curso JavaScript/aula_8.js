// Operadores Aritiméticos (matemáticos)

let salario = 100;

// + , - , * , / , **


console.log ( salario + salario )
console.log ( salario - salario )
console.log ( salario * salario )
console.log ( salario / salario )
console.log ( salario ** salario )


// Incremento ++ --

let idade = 18;


console.log ( ++idade  );
console.log ( idade );

console.log ( -- idade )
console.log ( idade )


