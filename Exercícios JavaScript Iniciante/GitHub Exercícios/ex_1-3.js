// Faça um programa que leia 3 números, alguns -os e exiba a média entre eles.

let num1 = Number ( prompt ( 'Numero 1: ' ) );
let num2 = Number ( prompt ( 'Numero 2: ' ) );
let num3 = Number ( prompt ( 'Numero 3: ' ) );

media = Number ( num1 + num2 + num3 ) / 3;

document.write ( 'A media entre os numeros e: ' , media );

