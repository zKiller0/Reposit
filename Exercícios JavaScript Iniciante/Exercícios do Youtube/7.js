// Operações Básicas 

// Váriaveis
let a , b;

// Entradas
a = parseInt ( prompt ( 'Digite um número: ') );
b = parseInt ( prompt ( 'Digite outro número: ' ) );

// Calculos
let soma = a + b;
let subtracao = a - b;
let multiplicar = a * b;
let divisao = a / b;
let resto = a % b;

// Exibindo
alert ( `A + B = ${soma}` );
alert ( `\nA - B = ${subtracao}` );
alert ( `\nA * B = ${multiplicar}` );
alert ( `\nA / B = ${divisao}` );
alert ( `\nO resto da divião de A e B = ${resto}` );
